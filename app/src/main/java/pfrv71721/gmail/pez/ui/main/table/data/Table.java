package pfrv71721.gmail.pez.ui.main.table.data;

public class Table {

    private int id;
    private int state;

    public Table(int id, int state) {
        this.id = id;
        this.state = state;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}



